%% =========================================================================
%  MPC控制器参数配置
%  纯电汽车热管理系统 - Gain Scheduling MPC
%  =========================================================================
%  使用前请根据实际AMESim模型参数修改以下数值
%  =========================================================================

function cfg = mpc_config()
%% ======================== MPC操纵变量(MV)约束 ===========================
%  MV = [rpm_comp; fan_cond; pump_battery; pump_motor]

cfg.mv_min = [1000;    % 压缩机最低转速 [rpm]
               0;      % 冷凝风扇最低 [%]
              10;      % 电池水泵最低 [%]（保持最小流量）
              10];     % 电机水泵最低 [%]（保持最小流量）

cfg.mv_max = [6000;    % 压缩机最高转速 [rpm]
              100;     % 冷凝风扇最高 [%]
              100;     % 电池水泵最高 [%]
              100];    % 电机水泵最高 [%]

% MV变化率限制 [每秒]
cfg.mv_rate = [500;    % 压缩机 [rpm/s]
               20;     % 冷凝风扇 [%/s]
               20;     % 电池水泵 [%/s]
               20];    % 电机水泵 [%/s]

%% ======================== MPC输出变量(OV)约束 ===========================
%  OV = [T_battery; T_cabin; T_coolant_inverter_out]

cfg.ov_min = [25;      % 电池温度下限 [℃]
              20;      % 座舱温度下限 [℃]
             -inf];   % 逆变器冷却液下限 [℃]（无下限）

cfg.ov_max = [35;      % 电池温度上限 [℃]
              26;      % 座舱温度上限 [℃]
              65];     % 逆变器冷却液上限 [℃]

cfg.ov_ref = [30;      % 电池温度目标 [℃]
              23;      % 座舱温度目标 [℃]
              45];     % 逆变器冷却液目标 [℃]

%% ======================== MPC基本参数 ==================================
cfg.Ts   = 5;          % 采样时间 [s]
cfg.Np   = 40;         % 预测时域 [步] → 200s
cfg.Nc   = 8;          % 控制时域 [步] → 40s

%% ======================== MPC权重矩阵 ==================================
%  基础权重（会被weight_scheduler动态调整）
cfg.Q_base = diag([10, 8, 2]);     % 输出跟踪权重 [T_batt, T_cabin, T_inv]
cfg.R_base = diag([0.1, 0.1, 0.1, 0.1]);  % 控制量变化权重
cfg.dQ_base = diag([0.5, 0.5, 0.5]);      % 输出变化率权重

%% ======================== PTC逻辑控制阈值 ==============================
%  电池PTC
cfg.ptc_batt_on  = 10;    % 电池温度低于此值开启 [℃]
cfg.ptc_batt_off = 15;    % 电池温度高于此值关闭 [℃]

%  座舱PTC
cfg.ptc_cabin_on  = -10;  % 环境温度低于此值开启 [℃]
cfg.ptc_cabin_off = 20;   % 座舱温度高于此值关闭 [℃]

%% ======================== 余热回收条件 =================================
cfg.heat_recovery_Tmin = 35;   % 逆变器冷却液温度阈值 [℃]

%% ======================== 模式切换温度阈值 ==============================
cfg.T_env_cold   = 0;     % 低于此值考虑制热 [℃]
cfg.T_env_hot    = 20;    % 高于此值考虑制冷 [℃]
cfg.T_batt_cold  = 15;    % 电池低温阈值 [℃]
cfg.T_batt_hot   = 32;    % 电池高温阈值 [℃]

%% ======================== 权重调度阈值 =================================
cfg.soc_low  = 0.30;     % SOC<30% → 能耗优先
cfg.soc_high = 0.70;     % SOC>70% → 舒适优先

%% ======================== 工作点参数（用于线性化） ======================
cfg.op.T_env        = -20;   % 工作点环境温度 [℃]
cfg.op.SOC          = 0.50;  % 工作点SOC
cfg.op.v_vehicle    = 60;    % 工作点车速 [km/h]
cfg.op.MotorTorque  = 80;    % 工作点电机扭矩 [Nm]

% 多工作点温度（用于Gain Scheduling）
cfg.op_temps = [-20, -10, 25, 35];  % [℃]

end
